#!/usr/bin/env node

import { existsSync, readFileSync, writeFileSync } from "fs"
import { dirname, join } from "path"
import { fileURLToPath } from "url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, "..")
const CONNECTOR_REGISTRY_PATH = join(ROOT, "connectors", "registry.json")
const OVERLAY_PATH = join(
  ROOT,
  "src",
  "lib",
  "platform",
  "registry.overlay.json"
)
const OUTPUT_PATH = join(
  ROOT,
  "src",
  "lib",
  "platform",
  "registry.generated.ts"
)

const HEADER = `// ============================================================================\n// DO NOT EDIT — generated by scripts/generate-platform-registry.js\n// Source: connectors/registry.json + src/lib/platform/registry.overlay.json\n// ============================================================================\n\n`

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"))
}

function formatString(value) {
  return JSON.stringify(value)
}

function unique(values) {
  return [...new Set(values)]
}

function requireConsumerMetadata(connector, overlayEntry) {
  const metadata = connector.consumerMetadata
  if (!metadata || typeof metadata !== "object") {
    throw new Error(
      `Connector ${connector.id} is missing consumerMetadata in connectors/registry.json`
    )
  }
  const required = ["sourceId", "displayName", "brandDomain", "defaultScope"]
  for (const field of required) {
    if (typeof metadata[field] !== "string" || metadata[field].trim() === "") {
      throw new Error(
        `Connector ${connector.id} is missing consumerMetadata.${field} required by ${overlayEntry.connectorId}`
      )
    }
  }
  return metadata
}

function platformIdsFor(connector, overlayEntry, id) {
  if (overlayEntry.platformIds?.length) {
    return overlayEntry.platformIds
  }
  const sourceId = connector.consumerMetadata?.sourceId
  return unique([connector.id, id, sourceId].filter(Boolean))
}

function generateEntry(connector, overlayEntry) {
  const metadata = requireConsumerMetadata(connector, overlayEntry)
  const id = overlayEntry.id ?? metadata.sourceId
  const fields = [
    `id: ${formatString(id)}`,
    `displayName: ${formatString(metadata.displayName)}`,
  ]
  if (metadata.brandDomain) {
    fields.push(`brandDomain: ${formatString(metadata.brandDomain)}`)
  }
  if (overlayEntry.iconKey ?? metadata.iconKey) {
    fields.push(
      `iconKey: ${formatString(overlayEntry.iconKey ?? metadata.iconKey)}`
    )
  }
  fields.push(
    `platformIds: [${platformIdsFor(connector, overlayEntry, id)
      .map(formatString)
      .join(", ")}]`
  )
  const aliases = unique([
    ...(metadata.aliases ?? []),
    ...(overlayEntry.aliases ?? []),
  ])
  if (aliases.length) {
    fields.push(`aliases: [${aliases.map(formatString).join(", ")}]`)
  }
  fields.push(`availability: "requiresConnector"`)
  if (typeof overlayEntry.showInConnectList === "boolean") {
    fields.push(`showInConnectList: ${String(overlayEntry.showInConnectList)}`)
  }
  const ingestScope = overlayEntry.defaultScope ?? metadata.defaultScope
  if (ingestScope) {
    fields.push(`ingestScope: ${formatString(ingestScope)}`)
  }
  return `  {\n    ${fields.join(",\n    ")}\n  }`
}

function main() {
  if (!existsSync(CONNECTOR_REGISTRY_PATH)) {
    throw new Error("connectors/registry.json is missing")
  }
  const connectorRegistry = readJson(CONNECTOR_REGISTRY_PATH)
  const overlay = readJson(OVERLAY_PATH)

  const overlayByConnectorId = new Map(
    overlay.connectors.map(entry => [entry.connectorId, entry])
  )

  const generatedEntries = []
  for (const connector of connectorRegistry.connectors) {
    const overlayEntry = overlayByConnectorId.get(connector.id)
    if (!overlayEntry) {
      continue
    }
    generatedEntries.push(generateEntry(connector, overlayEntry))
  }

  const output =
    HEADER +
    `import type { PlatformRegistryEntry } from "./registry";\n\n` +
    `export const PLATFORM_REGISTRY_GENERATED: PlatformRegistryEntry[] = [\n${generatedEntries.join(",\n")}\n];\n`

  writeFileSync(OUTPUT_PATH, output)
  console.log(`[generate-platform-registry] Wrote ${OUTPUT_PATH}`)
}

main()

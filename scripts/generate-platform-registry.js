import { existsSync, readFileSync, writeFileSync } from "fs"
import { dirname, join } from "path"
import { fileURLToPath } from "url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, "..")
const CONNECTOR_LOCK_PATH = join(ROOT, "connectors", "lock.json")
const OVERLAY_PATH = join(
  ROOT,
  "src",
  "lib",
  "platform",
  "registry.overlay.json"
)
const OUTPUT_PATH = join(
  ROOT,
  "src",
  "lib",
  "platform",
  "registry.generated.ts"
)

const HEADER = `// ============================================================================\n// DO NOT EDIT — generated by scripts/generate-platform-registry.js\n// Source: connectors/lock.json + bundled connector manifests + src/lib/platform/registry.overlay.json\n// ============================================================================\n\n`

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"))
}

function formatString(value) {
  return JSON.stringify(value)
}

function unique(values) {
  return [...new Set(values)]
}

function resolveConsumerMetadata(connectorManifest, overlayEntry) {
  const manifestMetadata =
    connectorManifest.consumer_metadata &&
    typeof connectorManifest.consumer_metadata === "object"
      ? connectorManifest.consumer_metadata
      : {}

  const metadata = {
    source_id: overlayEntry.id ?? connectorManifest.source_id ?? null,
    display_name: overlayEntry.displayName ?? connectorManifest.name ?? null,
    brand_domain: overlayEntry.brandDomain ?? manifestMetadata.brand_domain ?? null,
    aliases: unique([
      ...(manifestMetadata.aliases ?? []),
      ...(overlayEntry.aliases ?? []),
    ]),
    icon_key:
      overlayEntry.iconKey ??
      manifestMetadata.icon_key ??
      connectorManifest.source_id ??
      null,
    default_scope:
      overlayEntry.defaultScope ?? manifestMetadata.default_scope ?? null,
  }

  const required = ["source_id", "display_name", "brand_domain", "default_scope"]
  for (const field of required) {
    if (typeof metadata[field] !== "string" || metadata[field].trim() === "") {
      throw new Error(
        `Connector ${overlayEntry.connectorId} is missing resolved metadata.${field}`
      )
    }
  }
  return metadata
}

function platformIdsFor(connectorManifest, overlayEntry, id) {
  if (overlayEntry.platformIds?.length) {
    return overlayEntry.platformIds
  }
  return unique([
    connectorManifest.connector_id,
    id,
    connectorManifest.source_id,
  ].filter(Boolean))
}

function generateEntry(connectorManifest, overlayEntry) {
  const metadata = resolveConsumerMetadata(connectorManifest, overlayEntry)
  const id = overlayEntry.id ?? connectorManifest.source_id
  const fields = [
    `id: ${formatString(id)}`,
    `displayName: ${formatString(metadata.display_name)}`,
  ]
  if (metadata.brand_domain) {
    fields.push(`brandDomain: ${formatString(metadata.brand_domain)}`)
  }
  if (overlayEntry.iconKey ?? metadata.icon_key) {
    fields.push(
      `iconKey: ${formatString(overlayEntry.iconKey ?? metadata.icon_key)}`
    )
  }
  fields.push(
    `platformIds: [${platformIdsFor(connectorManifest, overlayEntry, id)
      .map(formatString)
      .join(", ")}]`
  )
  const aliases = unique(metadata.aliases ?? [])
  if (aliases.length) {
    fields.push(`aliases: [${aliases.map(formatString).join(", ")}]`)
  }
  fields.push(`availability: "requiresConnector"`)
  if (typeof overlayEntry.showInConnectList === "boolean") {
    fields.push(`showInConnectList: ${String(overlayEntry.showInConnectList)}`)
  }
  const ingestScope = overlayEntry.defaultScope ?? metadata.default_scope
  if (ingestScope) {
    fields.push(`ingestScope: ${formatString(ingestScope)}`)
  }
  return `  {\n    ${fields.join(",\n    ")}\n  }`
}

function main() {
  if (!existsSync(CONNECTOR_LOCK_PATH)) {
    throw new Error("connectors/lock.json is missing")
  }
  const connectorLock = readJson(CONNECTOR_LOCK_PATH)
  const overlay = readJson(OVERLAY_PATH)
  const manifestByConnectorId = new Map(
    connectorLock.connectors.map((entry) => {
      const manifestPath = join(ROOT, "connectors", entry.sourceFiles.metadata)
      return [entry.connectorId, readJson(manifestPath)]
    })
  )

  const generatedEntries = []
  for (const overlayEntry of overlay.connectors) {
    const connectorManifest = manifestByConnectorId.get(overlayEntry.connectorId)
    if (!connectorManifest) {
      throw new Error(
        `Missing bundled manifest for overlay connector ${overlayEntry.connectorId}`
      )
    }
    generatedEntries.push(generateEntry(connectorManifest, overlayEntry))
  }

  const output =
    HEADER +
    `import type { PlatformRegistryEntry } from "./registry";\n\n` +
    `export const PLATFORM_REGISTRY_GENERATED: PlatformRegistryEntry[] = [\n${generatedEntries.join(",\n")}\n];\n`

  writeFileSync(OUTPUT_PATH, output)
  console.log(`[generate-platform-registry] Wrote ${OUTPUT_PATH}`)
}

main()
